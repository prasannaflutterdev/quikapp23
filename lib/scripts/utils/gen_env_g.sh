EOF

log "âœ… env.g.dart generated successfully"

# Show first few lines for verification
log "ğŸ” Generated file preview:"
head -20 lib/config/env.g.dart | while IFS= read -r line; do
    log "   $line"
done

# Validate the generated file
if [ -f "lib/config/env.g.dart" ]; then
    log "âœ… env.g.dart file created successfully"
    
    # Check if Dart is available for analysis
    if command -v dart >/dev/null 2>&1; then
        if dart analyze lib/config/env.g.dart >/dev/null 2>&1; then
            log "âœ… Generated file passes Dart analysis"
        else
            log "âš ï¸ Generated file has Dart analysis issues (continuing anyway)"
        fi
    else
        log "âš ï¸ Dart not available for analysis (continuing anyway)"
    fi
else
    log "âŒ Failed to create env.g.dart file"
    exit 1
fi

log "ğŸ‰ env.g.dart generation completed successfully" 